package okis.mmpi.lb1;

public class ChartPoint {
	public double X, Y;

	public ChartPoint(double x, double y) {
		this.X = x;
		this.Y = y;
	}
	
	public double getX() {
		return X;
	}

	public void setX(double x) {
		X = x;
	}

	public double getY() {
		return Y;
	}

	public void setY(double y) {
		Y = y;
	}
}
